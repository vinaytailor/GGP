// For Loop
// for loop begins when x=2
// and runs till x <= 4
for (let x = 2; x <= 4; x++) {
    console.log("Value of x:" + x);
}

// For loop to print table of a number.
let x = 5
for (let i = 1; i <= 10; i++) {
  console.log(x * i); 
}

// For loop to print elements of an array.

let arr = [10, 20, 30, 40];
for (let i = 0; i < arr.length; i++) {
  console.log(arr[i]); 
}

